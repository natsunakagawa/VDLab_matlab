% handles    structure with handles and user data (see GUIDATA)

% Hint: get(hObject,'Value') returns toggle state of togglebutton1
handles = guihandles;
while get(hObject,'Value')
    
    curr = readCurrent(evalin('base','obj'));
    pos = readPosition(evalin('base','obj'));
    vel = readVelocity(evalin('base','obj'));

    set(handles.edit5, 'String', ['[ ', num2str(curr(1)), ' , ', num2str(curr(2)), ' ]']);
    set(handles.edit7, 'String', ['[ ', num2str(pos(1)), ' , ', num2str(pos(2)), ' ]']);
    set(handles.edit8, 'String', ['[ ', num2str(vel(1)), ' , ', num2str(vel(2)), ' ]']);
    
    pause(0.2)
end
